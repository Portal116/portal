package ex01;

public class SizeClass {
	public static void main(String[] args) {
		System.out.println("byte형의 크기 ===> " + Byte.SIZE);
		System.out.println("short형의 크기 ===> " + Short.SIZE);
		System.out.println("int형의 크기 ===> " + Integer.SIZE);
		System.out.println("long형의 크기 ===> " + Long.SIZE);
		System.out.println("float형의 크기 ===> " + Float.SIZE);
		System.out.println("double형의 크기 ===> " + Double.SIZE);
		System.out.println("char형의 크기 ===> " + Character.SIZE);
	}
}